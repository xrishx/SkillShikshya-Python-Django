x = 10
y = 4

# AND Operator
print("x & y = ", x & y)

# OR Operator
print("x | y = ", x | y)

# XOR Operator
print("x ^ y = ", x ^ y)

# NOT Operator
print(" ~x = ", ~ x)


#---Bitwise Shift---

# Left Shift
print("x << 1 =", x << 1)
print("y << 1 =", y << 1)

# Right Shift
print("x >> 1 =", x >> 1)
print("y >> 1 =", y >> 1)